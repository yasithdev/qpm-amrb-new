import torch
import torch.nn
import torch.nn.functional


class TestConvNet(torch.nn.Module):

  def __init__(self):
    super().__init__()
    self.conv1 = torch.nn.Conv2d(1, 10, kernel_size=5)
    self.conv2 = torch.nn.Conv2d(10, 20, kernel_size=5)
    self.conv2_drop = torch.nn.Dropout2d()
    self.fc1 = torch.nn.Linear(320, 50)
    self.fc2 = torch.nn.Linear(50, 10)

  def forward(self, x):
    x = torch.nn.functional.leaky_relu(torch.nn.functional.max_pool2d(self.conv1(x), 2))
    x = torch.nn.functional.leaky_relu(torch.nn.functional.max_pool2d(self.conv2_drop(self.conv2(x)), 2))
    x = x.view(-1, 320)
    x = torch.nn.functional.leaky_relu(self.fc1(x))
    x = torch.nn.functional.dropout(x, training=self.training)
    x = self.fc2(x)
    return torch.nn.functional.log_softmax(x, dim=1)
